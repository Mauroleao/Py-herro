# Adição
a = 10
b = 5
resultado_adicao = a + b
print("Adição:", resultado_adicao)  # Saída: 15

# Subtração
resultado_subtracao = a - b
print("Subtração:", resultado_subtracao)  # Saída: 5

# Multiplicação
resultado_multiplicacao = a * b
print("Multiplicação:", resultado_multiplicacao)  # Saída: 50

# Divisão
resultado_divisao = a / b
print("Divisão:", resultado_divisao)  # Saída: 2.0

# Divisão inteira
resultado_divisao_inteira = a // b
print("Divisão inteira:", resultado_divisao_inteira)  # Saída: 2

# Módulo
resultado_modulo = a % b
print("Módulo:", resultado_modulo)  # Saída: 0

# Exponenciação
resultado_exponenciacao = a ** b
print("Exponenciação:", resultado_exponenciacao)  # Saída: 100000